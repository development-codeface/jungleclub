									<h3>Top 12 Places to Visit in Wayanad</h3>
									
									<div class="tg-description">
										<p>Have you felt a slight nip in the air of late, especially during the evenings? Well, you’re not alone. The autumnal equinox is here: which means, we may officially say goodbye to the sultry summers for now and dust off the cozy woollens.</p><p>

And if you’re wondering what would be the ideal place to spend your vacation this season, let me suggest Wayanad. Wayanad is located at the southernmost tip of the Deccan Plateau and is a part of the vernal Western Ghats. Wayanad’s salubrious climate, its rich flora and fauna will make for a perfect retreat from the humdrum urban life you’ve been looking to escape.</p>
										
										<figure class="tg-blogdetailimg">
