								<div class="tg-detailbox">
									<h3>1. Chembra Peak</h3>
									<div class="tg-description">
										<p>
Chembra Peak is one of the most noted among Wayanad’s many marvels. Standing at a height of 2,100 metres, it is the highest peak in the region. Lush tea plantations surrounding the peak and the Hridayasarasu Lake close by offer uniquely beautiful and exhilarating views.</p>
									</div>


									<h3>2. Banasura Sagar Dam & Hill</h3>
									<div class="tg-description">
										<p>

The Banasura Sagar Dam is the largest earth dam in India and the second largest in all of Asia. Its project area also serves as the starting point for treks to the Banasura Peak. An interesting feature of this place is the set of islands that are formed upstream when the reservoir is full. These islands, with the Banasura hill in the backdrop, will mesmerise discerning visitors.</p>
									</div>




									<h3>3. Thirunelli Temple</h3>
									<div class="tg-description">
										<p>

Surrounded by mountains and beautiful thickets, Thirunelli Temple is an ancient temple in North Wayanad dedicated to Lord Vishnu.</p>
									</div>



									<h3>4. Lakkidi View Point</h3>
									<div class="tg-description">
										<p>

Lying atop Thamarassery at an elevation of 700 metres, Lakkidi is one of the highest locations in Wayanad commanding a picturesque scenery. The 12 km long journey from Adivaram to Lakkidi through thick forests is a riveting experience. It receives the second highest rainfall in the world and is home to different species of herons and other rare Indian birds.</p>
									</div>




									<h3>5. Edakkal Caves</h3>
									<div class="tg-description">
										<p>


Edakkal Caves are two natural caves in Ambukuthy mountain of Edakkal. Situated at 1,200 metres above sea level, a trek up here is a challenging one and not advisable for children and the elderly.</p>
									</div>




									<h3>6. Soochippara Falls</h3>
									<div class="tg-description">
										<p>


The waterfalls at Soochippara, ranging from 100 to 300 feet height, are a veritable treat to the eyes. The pool below is good for swimming, bathing and water rafting. The tree top huts give unique views of the surrounding valleys and glimmering waters from the springs</p>
									</div>



									<h3>7. Kuruvadweep
</h3>
									<div class="tg-description">
										<p>


The Kuruva island is spread over 950 acres of evergreen forest on the tributaries of Kabani River. It is a protected delta serving as a peaceful abode for rare species of flora and fauna.</p>
									</div>



									<h3>8. Pookode Lake</h3>
									<div class="tg-description">
										<p>


A natural freshwater lake adorned with evergreen mountains, Pookode Lake is another must-visit in Wayanad. The salacious weather, scenic beauty and crisp, unspoilt nature in Pookode Lake’s precincts will make your journey to Wayanad worthwhile.</p>
									</div>



									<h3>9. Wayanad Wildlife Sanctuary</h3>
									<div class="tg-description">
										<p>


Wayanad comes under the Western Ghats, which have now become renowned as World Heritage Site and the Hottest Hot Biodiversity Hotspot. Explore the wildlife of this region up close for an unforgettable rendezvous with a number of bird and animal species.</p>
									</div>



									<h3> 10. Jain Temple </h3>
									<div class="tg-description">
										<p>


This temple, said to have been built in the 13th century, is now mostly in ruins. It is a testimony to Jain architecture and the strong presence of Jain population in this region. The temple was taken over by Tipu Sultan and eventually got the name ‘Sultan Bathery’.</p>
									</div>


									<h3> 11. Neelimala </h3>
									<div class="tg-description">
										<p>



Located in southeastern Wayanad and accessible from Sulthan Bathery, Neelimala offers great options to trekking enthusiasts who will be aptly rewarded with breathtaking views, including that of the Meenmutty Falls, from atop the hill.</p>
									</div>


									
									<h3> 12. Pakshipathalam </h3>
									<div class="tg-description">
										<p>



Pakshipathalam is located deep within the forest of Brahmagiri hills at a height of more than 1,700 metres. The deep caves here are home to a diverse species of birds, animals and plants. A visit to the region would require a fascinating 7 km trek from Thirunelli dotted with forest.</p>
									</div>

